package Day_04;

public class Day_04_practice_13 {
//	관계연산자
	public static void main(String[] args) {
		int a=10;
		int b=20;
		
		System.out.println(a>b);//a보다 b가 크므로 false가 출력됨
		System.out.println(a<=b);//a보다 b가 크므로 true가 출력됨
		System.out.println(a==b);//a와 b가 같지 않으므로 false가 출력됨
		System.out.println(a!=b);//a와 b가 같지 않으므로 true가 출력됨
	}
}