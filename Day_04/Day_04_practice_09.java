package Day_04;

public class Day_04_practice_09 {
	public static void main(String[] args) {
//		산술 연산자 +,-,*,/,%
		int x=100;
		int y=200;
		
		System.out.println(x+y);//100+200의 값이 출력됨
		System.out.println(x-y);//100-200의 값이 출력됨
		System.out.println(x*y);//100*200의 값이 출력됨
		System.out.println(x/y);//100/200의 값이 출력됨. 원래는 0.5지만 소수점 아래는 버리므로 0이 출력됨
		System.out.println(x%y);//100%200의 값이 출력됨
	}
}