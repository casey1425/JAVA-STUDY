package Day_02;

public class Day_02_practice_06 {
	public static void main(String[] args) {
		System.out.printf("%.1f", 1.123456789);
		System.out.println();
		System.out.printf("%.2f", 1.123456789);
		System.out.println();
		System.out.printf("%.3f", 1.123456789);
		System.out.println();
		System.out.printf("%.4f", 1.123456789);
		System.out.println();
		System.out.printf("%.5f", 1.123456789);
		System.out.println();
	}
}
// %.nf형태는 소수점을 표기할때 사용된다.
// 예를 들어 %.2f는 소수점 아래 두 자리까지 표기할때 사용된다.