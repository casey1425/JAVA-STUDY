package Day_02;

public class Day_02_practice_05 {
	public static void main(String[] args) {
		System.out.printf("%5d", 1);
		System.out.println();
		System.out.printf("%5d", 12);
		System.out.println();
		System.out.printf("%5d", 123);
		System.out.println();
		System.out.printf("%5d", 1234);
		System.out.println();
		System.out.printf("%5d", 12345);
		System.out.println();
	}
}
// %nd는 n칸만큼 확보한 후, 오른쪽으로 정렬하여 출력한다는 뜻이다.
// 여기서 %5d로 입력하였으므로 5칸을 확보하여 오른쪽으로 정렬되었다.