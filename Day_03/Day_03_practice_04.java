package Day_03;

public class Day_03_practice_04 {
	public static void main(String[] args) {
		double pieDouble = 3.141592653589793;//double타입은 소수점 15자리까지 표현가능
		float pieFloat = 3.141592653589793f;//float타입은 소수점 7자리까지 표현가능
		
		System.out.println("double : "+pieDouble);
		System.out.println("float : "+pieFloat);
	}
}